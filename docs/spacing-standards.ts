/**
 * Dashboard Component Spacing Standards
 *
 * This document defines the consistent spacing patterns used across
 * all dashboard components for a cohesive design system.
 */

// ============================================================================
// HORIZONTAL PADDING (px-*)
// ============================================================================

/**
 * Main content areas and header:
 * - Mobile: px-4 (1rem / 16px)
 * - Tablet: sm:px-6 (1.5rem / 24px)
 * - Desktop: lg:px-8 (2rem / 32px)
 *
 * Used in: Header, DashboardLayout main content
 */

/**
 * Sidebar brand section:
 * - Mobile/Tablet: px-4 sm:px-6 (1rem / 1.5rem)
 * - Desktop: Same as mobile for consistency in narrow sidebar
 *
 * Used in: Sidebar brand/logo section
 */

/**
 * Navigation sections:
 * - px-3 (0.75rem / 12px) for nav container
 *
 * Used in: Sidebar navigation area
 */

/**
 * Compact areas (cards, footer):
 * - p-3 (0.75rem / 12px)
 *
 * Used in: Sidebar footer, dropdown menu padding
 */

// ============================================================================
// VERTICAL PADDING (py-*)
// ============================================================================

/**
 * Section padding:
 * - py-6 (1.5rem / 24px) for main sections
 *
 * Used in: DashboardLayout main, Sidebar nav container
 */

/**
 * Item padding:
 * - py-2.5 (0.625rem / 10px) for interactive items
 * - py-2 (0.5rem / 8px) for labels/headers
 *
 * Used in: Navigation items, dropdown menu items, section headers
 */

// ============================================================================
// GAPS (gap-*)
// ============================================================================

/**
 * Icon-to-text gap:
 * - gap-3 (0.75rem / 12px) standard icon spacing
 * - gap-2.5 (0.625rem / 10px) for compact layouts
 *
 * Used in: Navigation items, dropdown items, sidebar sections
 */

/**
 * Component gap:
 * - gap-4 (1rem / 16px) between major sections
 *
 * Used in: Header layout, between buttons and menus
 */

// ============================================================================
// VERTICAL SPACING (space-y-*)
// ============================================================================

/**
 * Navigation groups:
 * - space-y-6 (1.5rem / 24px) between nav groups
 * - space-y-1 (0.25rem / 4px) between nav items in a group
 *
 * Used in: Sidebar navigation structure
 */

/**
 * Dropdown sections:
 * - my-2 (0.5rem top/bottom) for separators
 * - gap-0.5 (0.125rem / 2px) for subtitle spacing
 *
 * Used in: Header dropdown menu
 */

// ============================================================================
// HEIGHTS
// ============================================================================

/**
 * Header/Brand height:
 * - h-16 (4rem / 64px) consistent header height
 *
 * Used in: Header, Sidebar brand section
 */

/**
 * Button/Interactive heights:
 * - h-10 (2.5rem / 40px) for buttons and triggers
 *
 * Used in: Dropdown triggers, buttons
 */

// ============================================================================
// WIDTHS
// ============================================================================

/**
 * Sidebar:
 * - Mobile: w-64 (16rem / 256px)
 * - Desktop: lg:w-56 (14rem / 224px)
 *
 * Rationale: Wider on mobile for better touch targets, narrower on desktop
 * to maximize content space
 */

/**
 * Dropdown menu:
 * - w-56 to w-64 (14rem to 16rem) for comfortable reading
 *
 * Used in: Header dropdown menu
 */
